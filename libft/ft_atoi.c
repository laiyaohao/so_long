#include "libft.h"

int	ft_atoi(const char *nptr)
{
	long	i;
	long	ans;
	int		sign;

	i = 0;
	ans = 0;
	sign = 1;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == 32)
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign = -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		ans = ans * 10 + (nptr[i] - 48);
		i++;
	}
	if (sign == -1)
		ans = ans * -1;
	return (ans);
}
